# Operator assignment adalah operator untuk memasukkan suatu nilai ke dalam variabel. 
# Dalam bahasa Python, operator assignment menggunakan tanda sama dengan (=).

a = 5
b = 3
b = b + 1
c = a + b
d = c + c + a
e = (c + d)* a
 
print('Isi variabel a:',a)
