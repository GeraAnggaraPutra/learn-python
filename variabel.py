# case sensitive
nama = "gera"
umur = 17
sukses = True

print("nama : " + nama)
print(umur)
print(sukses)

# sebuah variabel bisa ditimpa dengan nilai lain
nama = "jon"
print(nama)
nama = "key"
print(nama)

# sebuah variabel bisa diisi dengan berbagai tipe data
vr = "Learn python"
print(vr)
vr = 10.9
print(vr)
vr = False
print(vr)

# python tidak mengenal adanya konstata(variabel yg nilainya tidak bisa diubah)